{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_UART4_Init();
  EINK_Process();
	uint16_t cursor = 0;
  while (1)
  {
			uint8_t data = 0;
			while(HAL_UART_Receive(&huart4, &data, 1,100) != HAL_OK){}
		  if(data==0x55){
				uint8_t cmd = 0;
			  while(HAL_UART_Receive(&huart4, &cmd, 1,100) != HAL_OK){}
				if(cmd == 0)
				{
					data = cmd;
				}else if(cmd == 1){
					cursor = 0;
					EINK_Process();
				}
			}
			gImage_1in54[cursor++] = data;
			if(cursor==5000)cursor=0;
  }
}

